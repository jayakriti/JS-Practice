// =========================== WHILE LOOP =========================== //
let index = 0;
while(index <= 10)
{
    console.log(`Value of Index is: ${index}`);
    index = index+2;
}

console.log("------------------------------------------");

let myArray = ["Jaya", "Kriti", "Roma", "Rahul", "Rudra"];
let i = 0;
while(i < myArray.length)
{
    console.log(`Value is ${myArray[i]}`);
    i++;
}

console.log("------------------------------------------");

